    private void btnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseActionPerformed

        JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int returnVal = fc.showOpenDialog(this);

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            this.txtLocation.setText(file.getPath());
        }
}//GEN-LAST:event_btnBrowseActionPerformed

    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed

        if (this.txtName.getText().equals("")) {
            JOptionPane.showMessageDialog(null,
                    "Error: Name is invalid.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (this.txtLocation.getText().equals("")) {
            JOptionPane.showMessageDialog(null,
                    "Error: Location is invalid.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        String fileStr = this.txtLocation.getText() + "\\" +
                this.txtName.getText() + ".proj";

        if (new File(fileStr).exists()) {
            JOptionPane.showMessageDialog(null,
                    "Error: Project file already exists.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        try
        {
            FileWriter fstream = new FileWriter(fileStr);
            BufferedWriter out = new BufferedWriter(fstream);
            out.write("<src>\n</src>\n");
            out.close();
        }
        catch (Exception e)
        {
            System.err.println("Error: " + e.getMessage());
        }

        this.invoker.setCurrProjLoc(fileStr);

        this.setVisible(false);
    }//GEN-LAST:event_btnOkActionPerformed

